    private void showButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showButtonActionPerformed
        // TODO add your handling code here:
        searchtype = searchOptionCombo.getSelectedItem().toString();
        searchid = studentidtextfieldforsearch.getText();

        String quaryforsearch;
        ResultSet rs;

        if (searchtype.equals("All")) {
            try {

                DefaultTableModel tableModel = (DefaultTableModel) showStdData.showAllStudents();
                studentDataTable.setModel(tableModel);
                studentDataTable.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
                resizeTableColumn(studentDataTable);

            } catch (SQLException ex) {
                Logger.getLogger(SupervisorFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else if (searchtype.equals("Specific")) {
            if (searchid.equals("")) {
                JOptionPane.showMessageDialog(null, "Please enter a student Id");
            } else {

                try {
                    DefaultTableModel tableModel = (DefaultTableModel) showStdData.specificStudent(searchid);
                    studentDataTable.setModel(tableModel);
                    studentDataTable.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
                    resizeTableColumn(studentDataTable);

                } catch (SQLException ex) {
                    Logger.getLogger(SupervisorFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        } else if (searchtype.equals("Attatched")) {
            try {

                DefaultTableModel tableModel = (DefaultTableModel) showStdData.showAttachedStudents();
                studentDataTable.setModel(tableModel);
                studentDataTable.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
                resizeTableColumn(studentDataTable);

            } catch (SQLException ex) {
                Logger.getLogger(SupervisorFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (searchtype.equals("Resident")) {
            try {

                DefaultTableModel tableModel = (DefaultTableModel) showStdData.showResidentStudents();
                studentDataTable.setModel(tableModel);
                studentDataTable.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
                resizeTableColumn(studentDataTable);

            } catch (SQLException ex) {
                Logger.getLogger(SupervisorFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_showButtonActionPerformed

