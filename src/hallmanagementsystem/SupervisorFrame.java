    public void resizeTableColumn(JTable sstudentDataTable) {

        for (int column = 0; column < sstudentDataTable.getColumnCount(); column++) {
            TableColumn tableColumn = sstudentDataTable.getColumnModel().getColumn(column);

            int preferredWidth = tableColumn.getWidth();

            int maxWidth = tableColumn.getMaxWidth();

            Object value = tableColumn.getHeaderValue();
            TableCellRenderer renderer = tableColumn.getHeaderRenderer();

            if (renderer == null) {
                renderer = sstudentDataTable.getTableHeader().getDefaultRenderer();
            }

            Component hc = renderer.getTableCellRendererComponent(sstudentDataTable, value, false, false, -1, column);
            int headWidth = hc.getPreferredSize().width + sstudentDataTable.getIntercellSpacing().width;
            preferredWidth = Math.max(preferredWidth, headWidth);
            for (int row = 0; row < sstudentDataTable.getRowCount(); row++) {
                TableCellRenderer cellRenderer = sstudentDataTable.getCellRenderer(row, column);
                Component c = sstudentDataTable.prepareRenderer(cellRenderer, row, column);
                int width = c.getPreferredSize().width + sstudentDataTable.getIntercellSpacing().width;
                preferredWidth = Math.max(preferredWidth, width);

                if (preferredWidth >= maxWidth) {
                    preferredWidth = maxWidth;
                    break;
                }
                tableColumn.setPreferredWidth(preferredWidth);
            }
        }

    }

